function [A1,B1,C1] = sign2num(u)

  % element A 
    A = zeros(7,10);
    A(:,1) = [1 1 1 0 0 1 0]; 
    A(:,2) = [1 1 0 0 1 1 0]; 
    A(:,3) = [1 1 0 1 1 0 0]; 
    A(:,4) = [1 0 0 0 0 1 0]; 
    A(:,5) = [1 0 1 1 1 0 0]; 
    A(:,6) = [1 0 0 1 1 1 0]; 
    A(:,7) = [1 0 1 0 0 0 0]; 
    A(:,8) = [1 0 0 0 1 0 0]; 
    A(:,9) = [1 0 0 1 0 0 0]; 
    A(:,10) = [1 1 1 0 1 0 0]; 
    
    %element B
    B = zeros(7,10);
    B(:,1) = [1 0 1 1 0 0 0 ];
    B(:,2) = [1 0 0 1 1 0 0 ];
    B(:,3) = [1 1 0 0 1 0 0 ];
    B(:,4) = [1 0 1 1 1 1 0 ];
    B(:,5) = [1 1 0 0 0 1 0 ];
    B(:,6) = [1 0 0 0 1 1 0 ];
    B(:,7) = [1 1 1 1 0 1 0 ];
    B(:,8) = [1 1 0 1 1 1 0 ];
    B(:,9) = [1 1 1 0 1 1 0 ];
    B(:,10) = [1 1 0 1 0 0 0 ];

    % element C
    C = zeros(7,10);
    C(:,1) = [0 0 0 1 1 0 1];
    C(:,2) = [0 0 1 1 0 0 1];
    C(:,3) = [0 0 1 0 0 1 1];
    C(:,4) = [0 1 1 1 1 0 1];
    C(:,5) = [0 1 0 0 0 1 1];
    C(:,6) = [0 1 1 0 0 0 1];
    C(:,7) = [0 1 0 1 1 1 1];
    C(:,8) = [0 1 1 1 0 1 1];
    C(:,9) = [0 1 1 0 1 1 1];
    C(:,10) = [0 0 0 1 0 1 1];
    A1 = [];
    B1 = [];
    C1 = [];
    for j = 1:10
        for i =1 :7
            if A(i,j) ==1
                A1(j) =[A1 ones(1,u)]; 
                B1(j) = [B1(j) ones(1,u)];   
                C1(j) = [C1(j) ones(1,u)];   
            else
                A1(j) = [A1(j) zeros(1,u)];
                B1(j) = [B1(j) zeros(1,u)];
                C1(j) = [C1(j) zeros(1,u)];
            end
         end
    end

end